// What is .Net
// .NET is a Microsoft framework for building, deploying, and running applications.
// It includes a runtime (CLR) for managing code execution and a large class library (FCL) for common functionalities,
// supporting multiple programming languages and cross-platform development.


// .NET Framework vs .NET Core vs .NET
// .NET Framework: A Windows-only implementation of .NET primarily used for building Windows desktop and server-based applications.
// .NET Core: A cross-platform, open-source framework used for modern app development, supporting Windows, macOS, and Linux.
// .NET (or .NET 5+): The unified successor of .NET Core, combining features of both frameworks for cross-platform development.

